package day1;

public class RelationalOperator {
    public static void main(String[] args) {

        //comparison --return boolean-- true or false
        //< > >= <= == !=

        int i = 10;
        int j = 7;
        System.out.println(i>j);
        System.out.println(i<j);
        System.out.println(i>=j);
        System.out.println(i<=j);
        System.out.println(i==j);
        System.out.println(i!=j);
    }

}
