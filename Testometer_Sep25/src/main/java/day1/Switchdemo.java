package day1;

public class Switchdemo {
    public static void main(String[] args) {
        int daynumber=10;
        switch (daynumber){

            case  1:
            System.out.println("monday");
            break;
            case  2:
            System.out.println("Tuesday");
            break;

            default:
                System.out.println("No Day Found");
        }
    }
}
